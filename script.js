const EPOCH_START = 1625097600000; 

let player;
const audio = document.getElementById('audioOverlay');

function getCurrentStreamPosition() {
    const now = Date.now();
    const elapsedSeconds = Math.floor((now - EPOCH_START) / 1000);
    
    let totalDuration = 0;
    for (let video of videoData) {
        totalDuration += video.duration;
    }
    
    return elapsedSeconds % totalDuration;
}

function getVideoForCurrentTime(currentPosition) {
    let accumulatedDuration = 0;
    for (let video of videoData) {
        if (currentPosition < accumulatedDuration + video.duration) {
            return {
                video: video,
                startTime: currentPosition - accumulatedDuration
            };
        }
        accumulatedDuration += video.duration;
    }
    // This shouldn't happen, but just in case
    return { video: videoData[0], startTime: 0 };
}

function onPlayerReady(event) {
    const currentPosition = getCurrentStreamPosition();
    const { video, startTime } = getVideoForCurrentTime(currentPosition);
    
    player.loadVideoById({
        videoId: video.id,
        startSeconds: startTime
    });
    
    audio.currentTime = startTime % audio.duration;
    addLiveIndicator();
}

function onPlayerStateChange(event) {
	console.log(event.data);
    if (event.data == YT.PlayerState.ENDED) {
        const currentPosition = getCurrentStreamPosition();
        const { video, startTime } = getVideoForCurrentTime(currentPosition);
        
        player.loadVideoById({
            videoId: video.id,
            startSeconds: startTime
        });
    }
	if(event.data == YT.PlayerState.PAUSED){
		audio.pause();
	}
	else if(event.data == YT.PlayerState.PLAYING){
		audio.play();
	}
}

function addLiveIndicator() {
	if(document.getElementById('liveIndicator')) return;
    const liveIndicator = document.createElement('div');
	liveIndicator.id = 'liveIndicator';
    liveIndicator.innerHTML = 'LIVE';
    liveIndicator.style.cssText = `
        position: absolute;
        top: 10px;
        left: 10px;
        background-color: red;
        color: white;
        padding: 3px 5px;
        border-radius: 5px;
        font-weight: bold;
        z-index: 1000;
    `;
    document.body.appendChild(liveIndicator);
	// goes away after 3 seconds
	setTimeout(() => {
		document.body.removeChild(liveIndicator);
	}, 3000);
}

